                        'phone_number' => '(258) 84330678235'
                    ],
                    [
                        'country' => 'Uganda',
                        'state' => 'NOK',
                        'country_code' => '256',
                        'phone_number' => '(256) 7771031454'
                    ],
                    [
                        'country' => 'Ethiopia',
