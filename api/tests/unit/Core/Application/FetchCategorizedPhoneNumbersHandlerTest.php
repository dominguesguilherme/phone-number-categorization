                        'state' => 'OK',
                        'country_code' => '237',
                        'phone_number' => '(237) 677046616'
                    ],
                ],
            ],
        ];
    }

    /** @return Customer[] */
    private function data(): array
    {
        return [
            Customer::create(0 , 'Walid Hammadi', Phone::create('(212) 6007989253')),
